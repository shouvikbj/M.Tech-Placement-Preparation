#include<stdio.h>
#include<stdlib.h>
#include<math.h>

int main(){
    printf("%f\n",8.9);
    printf("%f\n",(5.0+4.5));
    printf("%f\n",(5+8.6));
    printf("%d\n",(5+8));
    printf("%d\n",(5/4));
    printf("%0.2f\n",(5/4.0));

    printf("%0.2f\n", pow(2,3));
    printf("%0.2f\n", sqrt(36));

    printf("%0.2f\n", ceil(36.356));
    printf("%0.2f\n", floor(36.356));

    return 0;
}